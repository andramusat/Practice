package cts.musat.andra.g1101.builder;

public class Podea {
    private String tip;
    private double duritate;

    public Podea(String tip, double duritate) {
        this.tip = tip;
        this.duritate = duritate;
    }

    public String getTip() {
        return tip;
    }

    public double getDuritate() {
        return duritate;
    }
}
